    /**
     * Finalizes current poly line being drawn on canvas after ENTER key press and confirm on dialog
     */
    public void finishCurrentPolyline() {
        Shape last = canvas.getShapes().isEmpty()?null:canvas.getLastShape();

        int select = JOptionPane.showConfirmDialog(this, "Do you want to finalize the current polyline?");
        if (select == JOptionPane.YES_OPTION && last instanceof Polyline){
            //finalize the current polyline
            Polyline current = (Polyline) last;

            current.setCompleted(true);  
            canvas.repaint();
            showFinalizePolylineText();
        
        }
    }
    
    /**
     * Displays an overlap error on the toolbar and hides it after 2 seconds, ensures any existing timers are stopped before creating a new one
     */
    private void showOverlapError(){
        overlapError.setVisible(true);
        
        //checks for and stops existing timers
        if (overlapTimer != null && overlapTimer.isRunning()) {
            overlapTimer.stop();
        }

        //create/reset timer
        overlapTimer = new Timer(2000, e -> {
            overlapError.setVisible(false);
            //repaint to ensure accurate display on gui
            canvas.repaint(); 
        });
        
        //no repeating
        overlapTimer.setRepeats(false);
        //start timer
        overlapTimer.start();       
    }
    
    /**
     * Update visibility of finalize poly line text based on canvas state
     */
    private void showFinalizePolylineText(){
        finalizePolylineText.setVisible(canvas.isDrawingPolyline());
    }
    
    /**
     * Validates canvas to ensure it is suitable for generating a fractal
     * @return true if canvas is valid for fractal generation, false otherwise
     */
    public boolean validCanvas(){
        for (Shape shape : canvas.getShapes()){
            if (shape instanceof Polyline l){
                if (!l.isCompleted()){
                    JOptionPane.showMessageDialog(this, "Cannot generate fractal with incomplete polylines.");
                    return false;
                }
            }
        }
        
        if (canvas.getShapes() == null || canvas.getShapes().isEmpty()){
            JOptionPane.showMessageDialog(this, "Cannot generate fractal with no shapes.");
            return false;
        }
        
        return true;
    }
    
