    /**
     * Updates position of last shape being drawn based on current mouse location, if mouse is within snap threshold, it snaps to the closest vertex
     * Also updates rectangle and triangle while drawing
     * @param evt 
     */
    private void canvasMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_canvasMouseDragged
        int x=evt.getX();
        int y=evt.getY();
        
        Shape last=canvas.getShapes().isEmpty()?null:canvas.getLastShape();
        
        //checks if point is snappable to the closest vertex
        Point mouse = new Point(x,y);
        Point snapPoint = canvas.snapToVertex(mouse, POLYGON_PROXIMITY_THRESHOLD);
              
        if (last instanceof Rectangle r) {
            r.setP2(snapPoint);
        }
        else if (last instanceof Triangle t){
            t.setP(snapPoint);
        }
        
        canvas.repaint();
    }//GEN-LAST:event_canvasMouseDragged

    /**
     * Processes user input to draw and edit shapes on canvas, including snapping, detecting overlaps, and adding new poly-lines or points
     * @param evt 
     */
    private void canvasMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_canvasMousePressed
        int x = evt.getX();
        int y = evt.getY();

        Shape last = canvas.getShapes().isEmpty()?null:canvas.getLastShape();
        
        //for snapping vertices
        Point mouse = new Point(x,y);
        Point snapPoint = canvas.snapToVertex(mouse, LINE_PROXIMITY_THRESHOLD);
        
        //if last edited/drawn shape was polyline
        if (last instanceof Polyline current) {

            //add new polyline if no incomplete polylines
            //this check is because the
            if (!current.isCompleted()) {
                //check for overlap
                if (current.hasOverlap(snapPoint, canvas)) {
                    showOverlapError();
                    return;
                }

                current.addLast(snapPoint);
            } 
            else {
                canvas.addNewShape(snapPoint.getX(), snapPoint.getY());  
            }
        } 
        //add new shape if there are no incomplete polylines
        else{
            canvas.addNewShape(snapPoint.getX(), snapPoint.getY());
        }
        canvas.repaint();
        showFinalizePolylineText();
        this.getRootPane().requestFocusInWindow();
    }//GEN-LAST:event_canvasMousePressed
  
