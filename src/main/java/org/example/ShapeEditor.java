    /**
     * Handles mouse released event, checks if last drawn shape overlaps with any other shape, and if so, shape is removed from canvas and overlap error is shown
     * @param evt 
     */
    private void canvasMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_canvasMouseReleased
        Shape last = canvas.getShapes().isEmpty() ? null : canvas.getLastShape();

        //rectangle check
        if (last instanceof Rectangle rect) {
            //if the rectangle overlaps with any other shape
            if (rect.hasOverlap(new Point(), canvas)) {
                canvas.getShapes().remove(last);
                showOverlapError();
            }
        } 
        else if (last instanceof Triangle tri) {
            //if the triangle overlaps with any other shape
            if (tri.hasOverlap(new Point(), canvas)) {
                canvas.getShapes().remove(last);
                showOverlapError();
            }
        }
        canvas.repaint();
    }//GEN-LAST:event_canvasMouseReleased

    /**
     * Handles mouse pressed event on openProject, open file chooser to let user select project file, and loads shape from the file if the file is valid
     * @param evt 
     */
    private void openProjectMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_openProjectMousePressed
        JFileChooser fc = new JFileChooser();
        fc.setDialogTitle("Open Project");
        int choice = fc.showOpenDialog(this);
        if (choice == JFileChooser.APPROVE_OPTION){
            File f = fc.getSelectedFile();
            ArrayList<Shape> shapes = ShapeIO.shapesFromFile(f.getAbsolutePath());
            if(shapes != null){
                canvas.setShapes(shapes);
                currentFilePath = f.getAbsolutePath();
            }
            else{
                JOptionPane.showMessageDialog(this, "Shapes cannot be loaded");
            }
        }
    }//GEN-LAST:event_openProjectMousePressed

