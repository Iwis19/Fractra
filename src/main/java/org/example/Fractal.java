    /**
     * Pains the component and renders shape and their fractal variations, if applicable, provides custom rendering for shapes stored in array list
     * @param g 
     */
    @Override
    public void paintComponent(Graphics g){
        super.paintComponent(g);
        //if no shapes, nothing has to be drawn
        if(shapes == null || shapes.isEmpty()){
            return;
        }
        //draw with graphics2d to prevent pixelated lines
        Graphics2D g2d = (Graphics2D)g;
        g2d.setRenderingHint(RenderingHints.KEY_ANTIALIASING,RenderingHints.VALUE_ANTIALIAS_ON);
        
        
        //iterate through all shapes
        for(Shape s : shapes){
            g2d.setColor(s.getColor());
            switch (s) {
                case Polyline polyline -> {
                    switch(polylineFractal){
                        case "None" -> s.paint(g2d);
                        case "Koch Curve" -> drawKoch(g2d, depth, polyline);
                        case "Levy C Curve" -> drawLevyCurve(g2d, depth, polyline);
                    }
                }
                case Rectangle rectangle -> {
                    switch(rectFractal){
                        case "None" -> s.paint(g2d);
                        case "T-Square" -> drawTSquare(g2d, depth, rectangle);
                        case "Sierpinski" -> drawSierpinskiCarpet(g2d, depth, rectangle);
                        case "Spiral" -> drawRectangleSpiral(g2d, depth, rectangle);
                    }
                }
                case Triangle triangle -> {
                    switch(triFractal){
                        case "None" -> s.paint(g2d);
                        case "Sierpinski" -> drawSierpinskiTriangle(g2d, depth, triangle);
                        case "Spiral" -> drawTriangleSpiral(g2d, depth, triangle);
                    }
                }
                default -> {
                }
            }
        } 
    }
    


    
